namespace Melanchall.DryWetMidi.MusicTheory
{
    /// <summary>
    /// Name of a note of the 12-tone chromatic scale.
    /// </summary>
    public enum  NoteName
    {
        /// <summary>
        /// C (Do) note.
        /// </summary>
        C = 0,

        /// <summary>
        /// Half-stepped C (Do) note.
        /// </summary>
        CSharp = 1,

        /// <summary>
        /// D (Re) note.
        /// </summary>
        D = 2,

        /// <summary>
        /// Half-stepped D (Re) note.
        /// </summary>
        DSharp = 3,

        /// <summary>
        /// E (Mi) note.
        /// </summary>
        E = 4,

        /// <summary>
        /// F (Fa) note.
        /// </summary>
        F = 5,

        /// <summary>
        /// Half-stepped F (Fa) note.
        /// </summary>
        FSharp = 6,

        /// <summary>
        /// G (Sol) note.
        /// </summary>
        G = 7,

        /// <summary>
        /// Half-stepped G (Sol) note.
        /// </summary>
        GSharp = 8,

        /// <summary>
        /// A (La) note.
        /// </summary>
        A = 9,

        /// <summary>
        /// Half-stepped A (La) note.
        /// </summary>
        ASharp = 10,

        /// <summary>
        /// B (Si) note.
        /// </summary>
        B = 11
    }
}
